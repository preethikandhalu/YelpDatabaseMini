CREATE TABLE YELP_USER_BUSINESS
( YELP_ID VARCHAR(4)
, CHECKED_IN_BUSINESS VARCHAR(4)
, PRIMARY KEY(YELP_ID, CHECKED_IN_BUSINESS)
, FOREIGN KEY(YELP_ID) REFERENCES YELP_USER ON DELETE CASCADE
);

CREATE TABLE BUSINESS
( BUSINESS_ID VARCHAR(4)
, BUSINESS_NAME VARCHAR(50)
, AMBIENT_TYPE VARCHAR(20)
, ADDRESS_STREET VARCHAR(50)
, ADDRESS_CITY VARCHAR(20)
, ADDRESS_STATE VARCHAR(2)
, ADDRESS_ZIPCODE INTEGER
, BUSINESS_CATEGORY VARCHAR(4)
, PRIMARY KEY(BUSINESS_ID)
);
 
CREATE TABLE BUSINESS_DAYS_OF_OP
( BUSINESS_ID VARCHAR(4)
, DAYS VARCHAR(4)
, PRIMARY KEY(BUSINESS_ID, DAYS)
, FOREIGN KEY(BUSINESS_ID) REFERENCES BUSINESS ON DELETE CASCADE
);

CREATE TABLE REVIEWS
( REVIEW_ID VARCHAR(4)
, STARS INTEGER
, YELP_ID VARCHAR(4)
, PUB_DATE DATE
, BUSINESS_ID VARCHAR(4)
, PRIMARY KEY(REVIEW_ID)
, FOREIGN KEY(YELP_ID) REFERENCES YELP_USER
, FOREIGN KEY(BUSINESS_ID) REFERENCES BUSINESS ON DELETE CASCADE
);

CREATE TABLE REVIEW_COMMENTS
( REVIEW_ID VARCHAR(4)
, COMMENTS VARCHAR(50)
, TEXTUAL_CONTENT VARCHAR(50)
, COMMENT_DATE DATE
, PRIMARY KEY(REVIEW_ID, COMMENTS)
, FOREIGN KEY(REVIEW_ID) REFERENCES REVIEWS ON DELETE CASCADE
);

CREATE TABLE BUSINESS_CATEGORY
( BUSINESS_CATEGORY_ID VARCHAR(4)
, BUSINESS_CATEGORY_NAME VARCHAR(50)
, PRIMARY KEY(BUSINESS_CATEGORY_ID)
);

CREATE TABLE BUSINESS_CATEGORY_SUBCATEGORY
( BUSINESS_CATEGORY_ID VARCHAR(4)
, SUBCATEGORY VARCHAR(50)
, PRIMARY KEY(BUSINESS_CATEGORY_ID, SUBCATEGORY)
, FOREIGN KEY(BUSINESS_CATEGORY_ID) REFERENCES BUSINESS_CATEGORY ON DELETE CASCADE
);

CREATE TABLE CHECK_IN
( CHECK_IN_ID VARCHAR(4)
, INFO VARCHAR(50)
, BUSINESS_ID VARCHAR(4)
, PRIMARY KEY(BUSINESS_ID, CHECK_IN_ID)
