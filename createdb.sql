);

CREATE TABLE YELP_USER_COMPLIMENT
( YELP_ID VARCHAR(4)
, COMPLEMENTED_FRIEND VARCHAR(4)
, PRIMARY KEY(YELP_ID, COMPLEMENTED_FRIEND)
